#include "ft_printf.h"

char	what_type(char *str, int i)
{
	char		c;

	if (!str)
		return (0);
	i++;
	while (str[i] != '\0')
	{
		c = str[i];
		if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' ||
			c == 'u' || c == 'x' || c == 'X' || c == '%')
			break ;
		i++;
	}
	return (c);
}
